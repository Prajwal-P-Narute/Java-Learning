import java.util.Scanner;
public class AsciiBetweenRange {
public static void main(String[] args) {
    
	Scanner sc = new Scanner(System.in);
	
	
	
	for(char ch = 'a' ;ch<='z'; ch++) {
		System.out.println((int)ch);
	}
	
	
	for(char ch = 'A' ;ch<='Z'; ch++) {
		System.out.println((int)ch);
	}
	
	
}
}
